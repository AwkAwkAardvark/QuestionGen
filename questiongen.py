def generate_question(state: QuestionState):
    type_spec = QUESTION_TYPES[state["QuestionTypeKey"]]

    prompt = f"""
You are an English exam question generator.

Create ONE question based on the paragraph.

Return the result using the required structured format.

General rules:
- Do not invent information outside the source paragraph.
- Do not silently repair, rewrite, or improve the source paragraph.
- The final student-facing question stem must be written in Korean.
- The explanation must be written entirely in Korean.
- The output field QuestionType must be exactly:
  "{type_spec.label_ko}"

Question type-specific rules:
{type_spec.rules}

OriginalQuestionNumber:
{state["OriginalQuestionNumber"]}

QuestionTypeKey:
{state["QuestionTypeKey"]}

QuestionType:
{type_spec.label_ko}

Source paragraph:
{state["source_paragraph"]}

Current student-facing paragraph:
{state["student_paragraph"]}
"""

    result = structured_llm.invoke(prompt)

    return {
        "student_paragraph": result.student_paragraph,
        "generated": result,
        "status": "generated",
        "errors": [],
    }

# ---------------------------------------
# Validation node
# ---------------------------------------

def normalize_text(text: str) -> str:
    return " ".join(text.split())


def validate_generated_question(state: QuestionState):
    generated = state["generated"]

    if generated is None:
        return {
            "status": "validation_error",
            "errors": ["No generated question returned."],
        }

    errors = []

    type_spec = QUESTION_TYPES[state["QuestionTypeKey"]]

    source_sentences = split_sentences(state["source_paragraph"])
    student_text = normalize_text(generated.student_paragraph)

    if generated.QuestionType != type_spec.label_ko:
        errors.append(
            f"QuestionType should be '{type_spec.label_ko}', but got '{generated.QuestionType}'."
        )

    if state["QuestionTypeKey"] == "sentence_insertion":
        if not generated.given_sentence:
            errors.append("given_sentence is missing.")

        else:
            given = normalize_text(generated.given_sentence)
            normalized_source = normalize_text(state["source_paragraph"])

            if given not in normalized_source:
                errors.append("given_sentence does not appear exactly in source_paragraph.")

            if given in student_text:
                errors.append("given_sentence still appears in student_paragraph.")

            for sentence in source_sentences:
                normalized_sentence = normalize_text(sentence)

                if normalized_sentence == given:
                    continue

                if normalized_sentence not in student_text:
                    errors.append(
                        f"Missing source sentence from student_paragraph: {normalized_sentence}"
                    )

            for marker in ["①", "②", "③", "④", "⑤"]:
                count = generated.student_paragraph.count(marker)
                if count != 1:
                    errors.append(
                        f"Marker {marker} appears {count} times, expected exactly once."
                    )

            if generated.choices != ["①", "②", "③", "④", "⑤"]:
                errors.append("choices must be exactly ['①', '②', '③', '④', '⑤'].")

            if generated.answer not in ["①", "②", "③", "④", "⑤"]:
                errors.append("answer must be one of ①, ②, ③, ④, ⑤.")

    if errors:
        return {
            "status": "validation_error",
            "errors": errors,
        }

    return {
        "status": "validation_passed",
        "errors": [],
    }

