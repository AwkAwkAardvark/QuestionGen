def split_sentences(text: str) -> list[str]:
    text = text.strip()

    if not text:
        return []

    sentences = re.split(r"(?<=[.!?])\s+", text)

    return [
        sentence.strip()
        for sentence in sentences
        if sentence.strip()
    ]

def check_eligibility(state: QuestionState):
    type_spec = QUESTION_TYPES[state["QuestionTypeKey"]]

    source_sentences = split_sentences(state["source_paragraph"])
    sentence_count = len(source_sentences)

    if type_spec.min_source_sentences is not None:
        if sentence_count < type_spec.min_source_sentences:
            return {
                "status": "input_error",
                "errors": [
                    f"{type_spec.label_ko} 유형은 최소 "
                    f"{type_spec.min_source_sentences}개의 문장이 필요하지만, "
                    f"현재 지문은 {sentence_count}개 문장으로 분석되었습니다."
                ],
            }

    return {
        "status": "input_passed",
        "errors": [],
    }

def route_after_eligibility(state: QuestionState):
    if state["status"] == "input_passed":
        return "generate_question"

    return END

